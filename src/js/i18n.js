function resolveLocale(locale) {
  return STRINGS[locale] ? locale : 'vi';
}

export function t(locale, key) {
  const resolved = resolveLocale(locale);
  return STRINGS[resolved][key] || STRINGS.vi[key] || key;
}

export function tf(locale, key, params = {}) {
  return t(locale, key).replace(/\{(\w+)\}/g, (_, token) => {
    return params[token] ?? `{${token}}`;
  });
}

export function applyI18n(root, locale) {
  const resolved = resolveLocale(locale);
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(resolved, el.dataset.i18n);
  });
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.setAttribute('placeholder', t(resolved, el.dataset.i18nPlaceholder));
  });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.setAttribute('title', t(resolved, el.dataset.i18nTitle));
  });
  document.documentElement.lang = resolved;
  document.title = t(resolved, 'document.title');
}
